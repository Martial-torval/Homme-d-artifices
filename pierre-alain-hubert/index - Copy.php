          <p class="text-justify lead p-2 description p-for-mobile mx-auto w-50" data-aos ="fade-up" data-aos-duration ="1000"><i>Parlons des feux d'artifice de jour qui constituent eux aussi une innovation.</i></p>
		  <p class="text-justify lead p-2 description p-for-mobile mx-auto w-50" data-aos ="fade-up" data-aos-duration ="1000">Ce sont mes maîtres japonais qui m'ont enseigné cette technique, mais l'idée m'avait effleuré lorsque je regardais
		  les négatifs de mes photos sur lequels se dessinent de magnifiques fleurs noires sur fond blanc (en 1974 j'ai édité un catalogue inversé). Je connaissais par ailleurs des bombes qui dispersaient des confettis. En 1977 j'ai fait mes
		  premiers voyages au Japon où la recherche avait particulièrement développé les signaux maritimes pour l'exportation. Il existait de nombreux types de fumigènes de couleur et les cehrcheurs planchaient sur des bombes au magnésium dont
		  l'éclat surpasserait la clarté du jour. Une autre idée était d'obscurcir le ciel avec des fumées sombres pour y tirer un feu d'artifice à l'intérieur. J'ai réuni de très belles photographies montrant ces fumigènes qui dessinent une
		  multitude de filaments noirs. Toute cette technologie est très coûteuse, car il est difficile de lutter avec le jour; on pouvait se le permettre au Japon ou à Eurodisney seulement.</p>
		  <p class="text-justify lead p-2 description p-for-mobile mx-auto w-50" data-aos ="fade-up" data-aos-duration ="1000">De temps en temps, j'ai des commandes de feux d'artifice de ce genre, parce qu'une inauguration doit impérativement
		  être programmée la journée. Il ne faut cependant pas trop en espérer; ils sont très pastels, or le public ici est habitué à ce que les feux d'artifice expriment la brutalité et non la poésie. Les Asiatiques, inversement, sont attentifs
		  au calme, ils font le salut les mains jointes devant la beauté. Je dois beaucoup à l'Asie. J'ai eu le privilège d'avoir été officiellement invité par l'Etat chinois à organiser un feu à Shanghai; c'est un grand honneur pour un Occidental.
		  Pour moi c'est une consécration de tirer un feu d'artifice dans le pays où l'on a inventé cet art au début de l'an mil.</p>

