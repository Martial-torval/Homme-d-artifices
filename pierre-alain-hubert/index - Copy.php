             <p class="text-justify lead p-2 description p-for-mobile  mx-auto w-50" style="text-align: center;">Les artificiers utilisent fréquemment les plans d'eau, car ils offrent des jeux de reflets qui démultiplient les effets en inventant des
			 signes innatendus, proches parfois de la calligraphie chinoise que je pratique. Il y a quelque chose d'extrêmement satisfaisant dans la rencontre en symétrie d'éléments antagonistes. L'eau introduit de plus la dimension de la lenteur,
			 du mouvement flottant qui apaise la déflagration. </p>
			 <p class="text-justify lead p-2 description p-for-mobile  mx-auto w-50" style="text-align: center;">Pour le final d'un de mes spectacles intitulé <i>Le ciel et l'eau étoilés ensemble</i>, j'ai chargé cent personnes d'allumer et de déposer
			 sur l'eau d'un lac des pots-torches flottants. Tout d'abord impersceptible, le courant disperse les points lumineux étoilant progressivement toute la surface de l'eau, noire comme le ciel.</p>
			 <p class="text-justify lead p-2 description p-for-mobile  mx-auto w-50" style="text-align: center;">Parfois des ponts m'ont servi de support, comme celui au-dessus d'un fleuve gelé à Calgary (Canada) en 1988, pour l'ouverture des jeux olympiques
			 d'hiver. Suivant une tradition très anglo-saxonne il fut organisé une vente aux enchères que je dus présider, en grand costume d'apparat bien sûr, pour réunir l'argent du feu d'artifice. A ceux qui voulaient bien donner de l'argent était remis un
			 certificat les assurant d'être le propriétaire de tel Bengale ou de telle fusée qui serait tiré bientôt par moi-même. On pouvait y lire : "A Bengal is not just a tiger, it's the name of a firework! And this certificate entitles you, to your very own Bengal fireworks".</p>
			 <p class="text-justify lead p-2 description p-for-mobile  mx-auto w-50" style="text-align: center;">Mon scénario « aquatique » préféré est celui écrit en 1980 pour le festival d'Avignon et le bicentenaire du 14 juillet. J'ai créé à cette occasion un pont de feu répondant au fameux pont interrompu d'Avignon.</p>

